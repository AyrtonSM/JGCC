package classes;

public class CodigoEndereco {
	
	private String sinal;
	private String op1;
	private String op2;
	private String simb;
	private String variableReferenced;
	
	public String getVariableReferenced() {
		return variableReferenced;
	}

	public void setVariableReferenced(String variableReferenced) {
		this.variableReferenced = variableReferenced;
	}

	public String getSinal() {
		return sinal;
	}
	
	public void setSinal(String sinal) {
		this.sinal = sinal;
	}
	
	public String getOp1() {
		return op1;
	}
	
	public void setOp1(String op1) {
		this.op1 = op1;
	}
	
	public String getOp2() {
		return op2;
	}
	
	public void setOp2(String op2) {
		this.op2 = op2;
	}
	
	public String getSimb() {
		return simb;
	}
	
	public void setSimb(String simb) {
		this.simb = simb;
	}
		
}
